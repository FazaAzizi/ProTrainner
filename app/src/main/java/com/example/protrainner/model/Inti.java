package com.example.protrainner.model;

public class Inti {
    String noInti;
    String jenisInti;
    String loadInti;
    String repsInti;
    String durasiInti;
    String setInti;

    public Inti() {
    }

    public Inti(String noInti, String jenisInti, String loadInti, String repsInti, String durasiInti, String setInti) {
        this.noInti = noInti;
        this.jenisInti = jenisInti;
        this.loadInti = loadInti;
        this.repsInti = repsInti;
        this.durasiInti = durasiInti;
        this.setInti = setInti;
    }

    public String getNoInti() {
        return noInti;
    }

    public void setNoInti(String noInti) {
        this.noInti = noInti;
    }

    public String getJenisInti() {
        return jenisInti;
    }

    public void setJenisInti(String jenisInti) {
        this.jenisInti = jenisInti;
    }

    public String getLoadInti() {
        return loadInti;
    }

    public void setLoadInti(String loadInti) {
        this.loadInti = loadInti;
    }

    public String getRepsInti() {
        return repsInti;
    }

    public void setRepsInti(String repsInti) {
        this.repsInti = repsInti;
    }

    public String getDurasiInti() {
        return durasiInti;
    }

    public void setDurasiInti(String durasiInti) {
        this.durasiInti = durasiInti;
    }

    public String getSetInti() {
        return setInti;
    }

    public void setSetInti(String setInti) {
        this.setInti = setInti;
    }
}
